// Sistema de Recomendação de Livros por Perfil de Cliente
export const getBookRecommendations = (clientProfile) => {
  const recommendations = [];
  
  if (clientProfile.numerology_number === 1 || clientProfile.decision_style?.includes('direto')) {
    recommendations.push({
      livro: SALES_BOOKS_LIBRARY.pitch_anything,
      motivo: "Cliente direto e assertivo responde a frame control e status"
    });
  }
  
  if (clientProfile.numerology_number === 3 || clientProfile.numerology_number === 7) {
    recommendations.push({
      livro: SALES_BOOKS_LIBRARY.spin_selling,
      motivo: "Cliente analítico precisa de lógica SPIN estruturada"
    });
  }
  
  if (clientProfile.status === 'quente' || clientProfile.pipeline_stage === 'negociacao') {
    recommendations.push({
      livro: SALES_BOOKS_LIBRARY.never_split,
      motivo: "Em negociação final, use táticas de ex-negociador FBI"
    });
  }
  
  if (!clientProfile.last_contact_date || clientProfile.status === 'frio') {
    recommendations.push({
      livro: SALES_BOOKS_LIBRARY.fanatical_prospecting,
      motivo: "Cliente frio precisa de abordagem multi-canal persistente"
    });
  }
  
  // Sempre incluir Cialdini - universal
  recommendations.push({
    livro: SALES_BOOKS_LIBRARY.cialdini_persuasao,
    motivo: "Gatilhos universais aplicáveis a qualquer cliente"
  });
  
  return recommendations;
};

